import cv2
import numpy as np

"""Pre-encoded image files into base64 string format"""

# Takes in file path to image and converts to encoded string
def encode_image_to_base64(filename):
    with open(filename, 'rb') as image_file:
        encoded_string = image_file.read().encode('base64')
        with open('encoded_data.txt', 'w') as fp:
            fp.write(encoded_string)
            return encoded_string

# Takes in encoded string and converts to image
def decode_image_from_base64(encoded_data):
    nparr = np.fromstring(encoded_data.decode('base64'), np.uint8)
    img = cv2.imdecode(nparr, cv2.IMREAD_COLOR)
    return img

